"""Phase 2 xG-map migration: native A/B plots + clustering checks.

This script consumes a Phase 1 coverage report generated by
`python -m BlackhawksSkillEstimation.compare_xg_coverage` and:

1) Produces native-resolution Cartesian heatmaps for sampled event_ids:
   - Overlap IDs: side-by-side (legacy vs new)
   - New-only / legacy-only IDs: single panel for the available source

2) Produces clustering CSVs (set-based SQL using EXISTS/NOT EXISTS):
   - Per-season top games by new_only_count and legacy_only_count
   - Per-season top players by new_only_count and legacy_only_count

Usage
-----
python -m BlackhawksSkillEstimation.plot_xg_map_migration_samples
python -m BlackhawksSkillEstimation.plot_xg_map_migration_samples --max-events 25
python -m BlackhawksSkillEstimation.plot_xg_map_migration_samples --report-json Data/Hockey/migration_reports/xg_coverage_YYYYMMDD_HHMMSS_report.json
"""

