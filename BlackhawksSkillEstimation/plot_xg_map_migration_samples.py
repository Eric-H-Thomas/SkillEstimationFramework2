def _normalize_shot_type(token: str) -> str:
    """Normalize a shot-type token for case/spacing differences.

    Example: "Wrist Shot" -> "wristshot", "snap-shot" -> "snapshot".
    """

    return "".join(ch for ch in (token or "").strip().lower() if ch.isalnum())


